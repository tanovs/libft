#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	size_t	i;
	char	*s;

	s = (char*)str;
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
			return (&s[i]);
		i++;
	}
	if (s[i] == (char)c)
		return (&s[i]);
	return (NULL);
}
